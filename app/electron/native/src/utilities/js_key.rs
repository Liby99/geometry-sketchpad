use core_ui::resources::Key;

pub fn u32_to_key(k: u32) -> Key {
  match k {
    8 => Key::Delete,
    16 => Key::LShift,
    17 => Key::LCtrl,
    18 => Key::LAlt,
    32 => Key::Space,
    48 => Key::D0,
    49 => Key::D1,
    50 => Key::D2,
    51 => Key::D3,
    52 => Key::D4,
    53 => Key::D5,
    54 => Key::D6,
    55 => Key::D7,
    56 => Key::D8,
    57 => Key::D9,
    65 => Key::A,
    66 => Key::B,
    67 => Key::C,
    68 => Key::D,
    69 => Key::E,
    70 => Key::F,
    71 => Key::G,
    72 => Key::H,
    73 => Key::I,
    74 => Key::J,
    75 => Key::K,
    76 => Key::L,
    77 => Key::M,
    78 => Key::N,
    79 => Key::O,
    80 => Key::P,
    81 => Key::Q,
    82 => Key::R,
    83 => Key::S,
    84 => Key::T,
    85 => Key::U,
    86 => Key::V,
    87 => Key::W,
    88 => Key::X,
    89 => Key::Y,
    90 => Key::Z,
    91 => Key::LCommand,
    93 => Key::RCommand,
    187 => Key::Equals,
    189 => Key::Minus,
    220 => Key::Backslash,
    _ => Key::Unknown,
  }
}